public class Loop548_self1 {
    public static void main(String[] args) {
        for (int i = 2; i <= 4; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.printf(i+" * "+j+" =%3d   ",i*j);
            }
            System.out.println();
            
        }
        
    }
}
